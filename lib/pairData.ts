export type PairType = 'forex' | 'crypto' | 'commodity';

export interface PairInfo {
  symbol: string;
  displayName: string;
  type: PairType;
  pipSize: number;          // e.g., 0.0001 for EUR/USD, 0.01 for JPY pairs
  pipValuePerLot: number;   // pip value in USD per standard lot (100k units)
  decimalPlaces: number;
  typicalSpread: number;    // in pips
  contractSize: number;     // 100000 for forex, 1 for crypto
  baseCurrency: string;
  quoteCurrency: string;
}

